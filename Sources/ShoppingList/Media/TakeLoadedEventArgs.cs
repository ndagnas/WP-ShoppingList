	//    #    #   #  #   #  #####         #####   ###   #   #  ####   #####  #### 

	//***************************************************************************************************************************
	// Classe TakeLoadedEventArgs
	//***************************************************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Permet de prévenir la fin du chargement d'un cliché d'article.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	public class TakeLoadedEventArgs : EventArgs
		{
		//***********************************************************************************************************************
		/// <summary>
		/// Initialise une nouvelle instance de l'objet <b>TakeLoadedEventArgs</b>.
		/// </summary>
		/// <param name="ArticleID">Identifiant de l'article.</param>
		/// <param name="Take">Cliché du produit.</param>
		//-----------------------------------------------------------------------------------------------------------------------
		public TakeLoadedEventArgs ( int ArticleID, BitmapImage Take )
			{
			//-------------------------------------------------------------------------------------------------------------------
			this.ArticleID = ArticleID;
			this.Take      = Take;
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Obtiens l'identifiant de l'article.
		/// </summary>
		/// <returns>Entier contenant l'identifiant de l'article.</returns>
		//-----------------------------------------------------------------------------------------------------------------------
		public int ArticleID { get; private set; }
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Obtiens le cliché de l'article.
		/// </summary>
		/// <returns>Objet <b>BitmapImage</b> contenant le cliché de l'article.</returns>
		//-----------------------------------------------------------------------------------------------------------------------
		public BitmapImage Take { get; private set; }
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
	//***************************************************************************************************************************

	} // Fin du namespace "ShoppingList.Media"
