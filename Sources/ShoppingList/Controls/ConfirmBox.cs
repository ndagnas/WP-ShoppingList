	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Fournit une boite de dialoque de type confirmation.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	public static class ConfirmBox
		{
		//***********************************************************************************************************************
		/// <summary>
		/// Obtiens une boite de dialoque de type confirmation.
		/// </summary>
		/// <param name="Text">Question à afficher.</param>
		/// <param name="Caption">Titre de la boite de dialogue.</param>
		/// <returns>
		/// <b>True</b> indique que l'utilisateur a cliqué sur Ok, sinon <b>False</b>.
		/// </returns>
		//-----------------------------------------------------------------------------------------------------------------------
		public static bool Show ( string Text, string Caption )
			{
			//-------------------------------------------------------------------------------------------------------------------
			return ( MessageBox.Show ( Text, Caption, MessageBoxButton.OKCancel ) == MessageBoxResult.OK );
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
