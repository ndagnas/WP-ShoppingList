	//***************************************************************************************************************************
	// Classe Article
	//***************************************************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Fournit l'accés à la table <b>Article</b> de la base de données <b>BddArticles</b>.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	[Table]public partial class Article : INotifyPropertyChanging, INotifyPropertyChanged
		{
		//-----------------------------------------------------------------------------------------------------------------------
		// Section des Attributs
		//-----------------------------------------------------------------------------------------------------------------------
		private int    FArticleID;
		private string FDescription = "";
		private bool   FIsFavory    = false;
		private int    FEntries     = 1;
		private int    FPurchases   = 0;
		//-----------------------------------------------------------------------------------------------------------------------
		
		//***********************************************************************************************************************
		#region // Définition des méthodes d'extensions
		//-----------------------------------------------------------------------------------------------------------------------

		//-----------------------------------------------------------------------------------------------------------------------
		partial void OnLoaded              (                     );
		partial void OnValidate            ( ChangeAction Action );
		partial void OnCreated             (                     );
		//-----------------------------------------------------------------------------------------------------------------------
		partial void OnArticleIDChanging   ( int Value           );
		partial void OnArticleIDChanged    (                     );
		//-----------------------------------------------------------------------------------------------------------------------
		partial void OnDescriptionChanging ( string Value        );
		partial void OnDescriptionChanged  (                     );
		//-----------------------------------------------------------------------------------------------------------------------
		partial void OnIsFavoryChanging    ( bool Value          );
		partial void OnIsFavoryChanged     (                     );
		//-----------------------------------------------------------------------------------------------------------------------
		partial void OnEntriesChanging     ( int Value           );
		partial void OnEntriesChanged      (                     );
		//-----------------------------------------------------------------------------------------------------------------------
		partial void OnPurchasesChanging   ( int Value           );
		partial void OnPurchasesChanged    (                     );
		//-----------------------------------------------------------------------------------------------------------------------

		//-----------------------------------------------------------------------------------------------------------------------
		#endregion
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Initialise une nouvelle instance de l'objet <b>Article</b>.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public Article () { OnCreated (); }
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Indique qu'une des valeurs s'apprète à être modifiée.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		protected virtual void SendPropertyChanging ()
			{
			//-------------------------------------------------------------------------------------------------------------------
			if ( this.PropertyChanging != null )
				this.PropertyChanging ( this, new PropertyChangingEventArgs ( String.Empty ) );
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Indique qu'une des valeurs a été modifiée.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		protected virtual void SendPropertyChanged ( string PropertyName )
			{
			//-------------------------------------------------------------------------------------------------------------------
			if ( this.PropertyChanged != null )
				this.PropertyChanged ( this, new PropertyChangedEventArgs ( PropertyName ) );
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Accède à la propriété <b>ArticleID</b> de la table.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		[Column(Storage = "FArticleID", AutoSync = AutoSync.OnInsert, 
		              DbType = "Int NOT NULL IDENTITY", IsPrimaryKey = true, IsDbGenerated = true)]
		//-----------------------------------------------------------------------------------------------------------------------
		public int ArticleID
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return this.FArticleID; }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set
				{
				//---------------------------------------------------------------------------------------------------------------
				if ( this.FArticleID != value )
					{
					//-----------------------------------------------------------------------------------------------------------
					this.OnArticleIDChanging  ( value );
					this.SendPropertyChanging (       );

					this.FArticleID = value;

					this.SendPropertyChanged ( "ArticleID" );
					this.OnArticleIDChanged  (             );
					//-----------------------------------------------------------------------------------------------------------
					}
				//---------------------------------------------------------------------------------------------------------------
				}
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************
		
		//***********************************************************************************************************************
		/// <summary>
		/// Accède à la propriété <b>Description</b> de la table.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		[Column(Storage = "FDescription", DbType = "NVarChar(50) NOT NULL")]
		//-----------------------------------------------------------------------------------------------------------------------
		public string Description
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return this.FDescription; }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set
				{
				//---------------------------------------------------------------------------------------------------------------
				if ( this.FDescription != value )
					{
					//-----------------------------------------------------------------------------------------------------------
					this.OnDescriptionChanging ( value );
					this.SendPropertyChanging  (       );

					this.FDescription = value;

					this.SendPropertyChanged  ( "Description" );
					this.OnDescriptionChanged (               );
					//-----------------------------------------------------------------------------------------------------------
					}
				//---------------------------------------------------------------------------------------------------------------
				}
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Accède à la propriété <b>IsFavory</b> de la table.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		[Column(Storage = "FIsFavory", DbType = "Bit NOT NULL")]
		//-----------------------------------------------------------------------------------------------------------------------
		public bool IsFavory
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return this.FIsFavory; }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set
				{
				//---------------------------------------------------------------------------------------------------------------
				if ( this.FIsFavory != value )
					{
					//-----------------------------------------------------------------------------------------------------------
					this.OnIsFavoryChanging   ( value );
					this.SendPropertyChanging (       );

					this.FIsFavory = value;

					this.SendPropertyChanged ( "IsFavory" );
					this.OnIsFavoryChanged   (            );
					//-----------------------------------------------------------------------------------------------------------
					}
				//---------------------------------------------------------------------------------------------------------------
				}
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************
		
		//***********************************************************************************************************************
		/// <summary>
		/// Accède à la propriété <b>Entries</b> de la table.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		[Column(Storage = "FEntries", DbType = "Int NOT NULL")]
		//-----------------------------------------------------------------------------------------------------------------------
		public int Entries
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return this.FEntries; }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set
				{
				//---------------------------------------------------------------------------------------------------------------
				if ( this.FEntries != value )
					{
					//-----------------------------------------------------------------------------------------------------------
					this.OnEntriesChanging    ( value );
					this.SendPropertyChanging (       );

					this.FEntries = value;

					this.SendPropertyChanged ( "Entries" );
					this.OnEntriesChanged    (           );
					//-----------------------------------------------------------------------------------------------------------
					}
				//---------------------------------------------------------------------------------------------------------------
				}
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************
		
		//***********************************************************************************************************************
		/// <summary>
		/// Accède à la propriété <b>Purchases</b> de la table.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		[Column(Storage = "FPurchases", DbType = "Int NOT NULL")]
		//-----------------------------------------------------------------------------------------------------------------------
		public int Purchases
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return this.FPurchases; }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set
				{
				//---------------------------------------------------------------------------------------------------------------
				if ( this.FPurchases != value )
					{
					//-----------------------------------------------------------------------------------------------------------
					this.OnPurchasesChanging  ( value );
					this.SendPropertyChanging (       );

					this.FPurchases = value;

					this.SendPropertyChanged ( "Purchases" );
					this.OnPurchasesChanged  (             );
					//-----------------------------------------------------------------------------------------------------------
					}
				//---------------------------------------------------------------------------------------------------------------
				}
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************
		
		//***********************************************************************************************************************
		/// <summary>
		/// Se produit lorsque qu'une propriété va être modifiée.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public event PropertyChangingEventHandler PropertyChanging;
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Se produit lorsque qu'une propriété a été modifiée.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public event PropertyChangedEventHandler PropertyChanged;
		//***********************************************************************************************************************
		}
