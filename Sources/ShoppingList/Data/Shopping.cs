	//   ###   #####  #   #  ####   ####   #  # # #  #     
	//      #  #   #  #   #  #      #      #  #  ##  #   ##
	//  ####   #   #   ###   #      #      #  #   #   ### #

	//***************************************************************************************************************************
	// Classe Shopping
	//***************************************************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Fournit l'accés à la table <b>Shopping</b> de la base de données <b>BddArticles</b>.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	[Table]public partial class Shopping : INotifyPropertyChanging, INotifyPropertyChanged
		{
		//-----------------------------------------------------------------------------------------------------------------------
		// Section des Attributs
		//-----------------------------------------------------------------------------------------------------------------------
		private int      FArticleID  = 0;
		private int      FQuantity   = 1;
		private bool     FIsChecked  = false;
		private DateTime FInsertDate = DateTime.Now;
		//-----------------------------------------------------------------------------------------------------------------------
		
		//***********************************************************************************************************************
		#region // Définition des méthodes d'extensions
		//-----------------------------------------------------------------------------------------------------------------------

		//-----------------------------------------------------------------------------------------------------------------------
		partial void OnLoaded             (                     );
		partial void OnValidate           ( ChangeAction Action );
		partial void OnCreated            (                     );
		//-----------------------------------------------------------------------------------------------------------------------
		partial void OnArticleIDChanging  ( int Value           );
		partial void OnArticleIDChanged   (                     );
		//-----------------------------------------------------------------------------------------------------------------------
		partial void OnQuantityChanging   ( int Value           );
		partial void OnQuantityChanged    (                     );
		//-----------------------------------------------------------------------------------------------------------------------
		partial void OnIsCheckedChanging  ( bool Value          );
		partial void OnIsCheckedChanged   (                     );
		//-----------------------------------------------------------------------------------------------------------------------
		partial void OnInsertDateChanging ( DateTime Value      );
		partial void OnInsertDateChanged  (                     );
		//-----------------------------------------------------------------------------------------------------------------------

		//-----------------------------------------------------------------------------------------------------------------------
		#endregion
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		// Prototype   : public Shopping ()
		// Description : Constructeur de la classe
		//***********************************************************************************************************************
		/// <summary>
		/// Initialise une nouvelle instance de l'objet <b>Shopping</b>.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public Shopping () { OnCreated (); }
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		// Prototype   : protected virtual void SendPropertyChanging ()
		// Description : Indique qu'une des valeurs s'apprète à être modifiée
		//***********************************************************************************************************************
		/// <summary>
		/// Indique qu'une des valeurs s'apprète à être modifiée.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		protected virtual void SendPropertyChanging ()
			{
			//-------------------------------------------------------------------------------------------------------------------
			if ( this.PropertyChanging != null )
				this.PropertyChanging ( this, new PropertyChangingEventArgs ( String.Empty ) );
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		// Prototype   : protected virtual void SendPropertyChanged ( string PropertyName )
		// Description : Indique qu'une des valeurs a été modifiée
		//***********************************************************************************************************************
		/// <summary>
		/// Indique qu'une des valeurs a été modifiée.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		protected virtual void SendPropertyChanged ( string PropertyName )
			{
			//-------------------------------------------------------------------------------------------------------------------
			if ( this.PropertyChanged != null )
				this.PropertyChanged ( this, new PropertyChangedEventArgs ( PropertyName ) );
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************
		
		//***********************************************************************************************************************
		/// <summary>
		/// Accède à la propriété <b>ArticleID</b> de la table.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		[Column(Storage = "FArticleID", DbType = "Int NOT NULL", IsPrimaryKey = true)]
		//-----------------------------------------------------------------------------------------------------------------------
		public int ArticleID
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return this.FArticleID; }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set
				{
				//---------------------------------------------------------------------------------------------------------------
				if ( this.FArticleID != value )
					{
					//-----------------------------------------------------------------------------------------------------------
					this.OnArticleIDChanging  ( value );
					this.SendPropertyChanging (       );

					this.FArticleID = value;

					this.SendPropertyChanged ( "ArticleID" );
					this.OnArticleIDChanged  (             );
					//-----------------------------------------------------------------------------------------------------------
					}
				//---------------------------------------------------------------------------------------------------------------
				}
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************
		
		//***********************************************************************************************************************
		/// <summary>
		/// Accède à la propriété <b>Quantity</b> de la table.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		[Column(Storage = "FQuantity", DbType = "Int NOT NULL")]
		//-----------------------------------------------------------------------------------------------------------------------
		public int Quantity
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return this.FQuantity; }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set
				{
				//---------------------------------------------------------------------------------------------------------------
				if ( this.FQuantity != value )
					{
					//-----------------------------------------------------------------------------------------------------------
					this.OnQuantityChanging   ( value );
					this.SendPropertyChanging (       );

					this.FQuantity = value;

					this.SendPropertyChanged ( "Quantity" );
					this.OnQuantityChanged   (            );
					//-----------------------------------------------------------------------------------------------------------
					}
				//---------------------------------------------------------------------------------------------------------------
				}
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************
		
		//***********************************************************************************************************************
		/// <summary>
		/// Accède à la propriété <b>IsChecked</b> de la table.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		[Column(Storage = "FIsChecked", DbType = "Bit NOT NULL")]
		//-----------------------------------------------------------------------------------------------------------------------
		public bool IsChecked
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return this.FIsChecked; }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set
				{
				//---------------------------------------------------------------------------------------------------------------
				if ( this.FIsChecked != value )
					{
					//-----------------------------------------------------------------------------------------------------------
					this.OnIsCheckedChanging  ( value );
					this.SendPropertyChanging (       );

					this.FIsChecked = value;

					this.SendPropertyChanged ( "IsChecked" );
					this.OnIsCheckedChanged  (             );
					//-----------------------------------------------------------------------------------------------------------
					}
				//---------------------------------------------------------------------------------------------------------------
				}
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************
		
		//***********************************************************************************************************************
		/// <summary>
		/// Accède à la propriété <b>InsertDate</b> de la table.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		[Column(Storage = "FInsertDate", DbType = "DateTime NOT NULL")]
		//-----------------------------------------------------------------------------------------------------------------------
		public DateTime InsertDate
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return this.FInsertDate; }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set
				{
				//---------------------------------------------------------------------------------------------------------------
				if ( this.FInsertDate != value )
					{
					//-----------------------------------------------------------------------------------------------------------
					this.OnInsertDateChanging ( value );
					this.SendPropertyChanging (       );

					this.FInsertDate = value;

					this.SendPropertyChanged ( "InsertDate" );
					this.OnInsertDateChanged (              );
					//-----------------------------------------------------------------------------------------------------------
					}
				//---------------------------------------------------------------------------------------------------------------
				}
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Se produit lorsque qu'une propriété va être modifiée.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public event PropertyChangingEventHandler PropertyChanging;
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Se produit lorsque qu'une propriété a été modifiée.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public event PropertyChangedEventHandler PropertyChanged;
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
	//***************************************************************************************************************************

	} // Fin du namespace "ShoppingList.Data"
