	//  #   #  #      #             #   #  #   #  #   #
	//  #   #  #      #             ####   #   #  #   #

	//***************************************************************************************************************************
	// Classe ApplicationBar
	//***************************************************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Gère la barre d'icône d'une popup.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	[ContentProperty("Buttons")]
	//---------------------------------------------------------------------------------------------------------------------------
	public class ApplicationBar : ItemsControl
		{
		//-----------------------------------------------------------------------------------------------------------------------
		// Section des Attributs
		//-----------------------------------------------------------------------------------------------------------------------
		private List<ApplicationBarButton> FButtons = new List<ApplicationBarButton> ();
		//-----------------------------------------------------------------------------------------------------------------------

		//***********************************************************************************************************************
		/// <summary>
		/// Accède à la liste des icônes.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public IList Buttons { get { return this.FButtons; } }
		//***********************************************************************************************************************
 
		//***********************************************************************************************************************
		/// <summary>
		/// Accède à une icône à partir de son nom.
		/// </summary>
		/// <param name="Name">Nom de l'icône.</param>
		/// <returns>Icône associée.</returns>
		//-----------------------------------------------------------------------------------------------------------------------
		public ApplicationBarButton this[string Name]
			{
			//-------------------------------------------------------------------------------------------------------------------
			get
				{
				//---------------------------------------------------------------------------------------------------------------
				foreach ( ApplicationBarButton Button in FButtons )
					{
					if ( Button.Name.Equals ( Name ) ) return Button;
					}

				return null;
				//---------------------------------------------------------------------------------------------------------------
				}
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
	//***************************************************************************************************************************

	} // Fin du namespace "ShoppingList.Popups.Primitives"
